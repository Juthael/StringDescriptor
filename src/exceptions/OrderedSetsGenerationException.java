package exceptions;

public class OrderedSetsGenerationException extends Exception {

	private static final long serialVersionUID = 1240532842944643955L;

	public OrderedSetsGenerationException() {
	}

	public OrderedSetsGenerationException(String arg0) {
		super(arg0);
	}

	public OrderedSetsGenerationException(Throwable arg0) {
		super(arg0);
	}

	public OrderedSetsGenerationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public OrderedSetsGenerationException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
