package exceptions;

public class SynTreeGenerationException extends Exception {

	private static final long serialVersionUID = -4469903173051482351L;

	public SynTreeGenerationException() {
	}

	public SynTreeGenerationException(String arg0) {
		super(arg0);
	}

	public SynTreeGenerationException(Throwable arg0) {
		super(arg0);
	}

	public SynTreeGenerationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public SynTreeGenerationException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
